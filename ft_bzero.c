#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*text;

	text = (unsigned char *)s;
	i = 0;
	while (i != n)
	{
		text[i] = '\0';
		i++;
	}
}
