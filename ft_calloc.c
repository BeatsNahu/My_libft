#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*ptr;
	size_t	limit;

	limit = (size_t) -1;
	if (nmemb <= 0 || size <= 0)
	{
		ptr = malloc(1);
		return (ptr);
	}
	if (size > limit / nmemb)
		return (NULL);
	ptr = malloc(nmemb * size);
	if (!ptr)
		return (NULL);
	ft_bzero(ptr, nmemb * size);
	return (ptr);
}
